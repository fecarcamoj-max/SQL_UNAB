DROP DATABASE IF EXISTS apps_and_crunchbase;
CREATE DATABASE apps_and_crunchbase DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci;
USE apps_and_crunchbase;

CREATE TABLE categorias (
    categoria VARCHAR(5) PRIMARY KEY,
    category_name VARCHAR(30) NOT NULL,
    matriz VARCHAR(5)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE aplicaciones (
    id INT(12) PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    name_length SMALLINT(5),
    release_date DATE,
    dispositivos SMALLINT(5),
    game_center TINYINT(1),
    capturas_pantalla TINYINT(3),
    screenshots_ipad TINYINT(3),
    desarrollador BIGINT(15),
    developer_name VARCHAR(100),
    seller_name VARCHAR(100),
    seller_url VARCHAR(100),
    precio DECIMAL(5,2),
    num_of_categories TINYINT(3),
    category_primary VARCHAR(30),
    lenguajes TINYINT(3),
    tamano_archivo BIGINT(15),
    description_length INT(10),
    content_rating VARCHAR(3),
    INDEX idx_nombre (nombre),
    INDEX idx_precio (precio),
    INDEX idx_category_primary (category_primary),
    INDEX idx_desarrollador (desarrollador),
    INDEX idx_developer_name (developer_name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE app_reviews (
    id INT(12) PRIMARY KEY,
    rating_count INT(10),
    average_rating FLOAT,
    CONSTRAINT fk_reviews_app
        FOREIGN KEY (id) REFERENCES aplicaciones(id)
        ON DELETE CASCADE
        ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE app_categories (
    id INT(12),
    desarrollador BIGINT(15),
    categoria VARCHAR(5),
    primario TINYINT(1),
    PRIMARY KEY(id, categoria),
    INDEX idx_app_categories_desarrollador (desarrollador),
    INDEX idx_app_categories_categoria (categoria),
    INDEX idx_app_categories_primario (primario),
    CONSTRAINT fk_cat_app
        FOREIGN KEY (id) REFERENCES aplicaciones(id)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT fk_categoria
        FOREIGN KEY (categoria) REFERENCES categorias(categoria)
        ON DELETE RESTRICT
        ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE top300 (
    idx INT(11) AUTO_INCREMENT PRIMARY KEY,
    insert_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    lista VARCHAR(20),
    id INT(12),
    INDEX idx_top300_lista (lista),
    INDEX idx_top300_id (id),
    CONSTRAINT fk_top_app
        FOREIGN KEY (id) REFERENCES aplicaciones(id)
        ON DELETE CASCADE
        ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

